package it.unibo.objectmon.model.battle.moves.type;

/**
 * Move represents a type of move in the battle.
 */
public enum Move {
    /**
     * type of attack move.
     */
    ATTACK,
    /**
     * type of use item move.
     */
    USE_ITEM,
    /**
     * type of run away.
     */
    RUN_AWAY,
    /**
     * type of switch objectmon.
     */
    SWITCH_OBJECTMON
}
